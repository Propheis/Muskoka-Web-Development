            <h2 id="EncryptionCrackingTool">Encryption Cracking Tool</h2>
            <div>
              <table>
                <tr>
                  <th>Client:</th>
                  <td>N/A</td>
                </tr>
                <tr>
                  <th>What I Did: </th>
                  <td>Wrote a script to crack a simple encryption as a learning exercise</td>
                </tr>
                <tr>
                  <th>What I Learned: </th>
                  <td>Security through obscurity is not enough. To safely encrypt a document the system being used should have minimal "collisions" (i.e. input should produce at most 1 unique output)</td>
                </tr>
                <tr>
                  <th>Skills/Tech Used:</th>
                  <td>
                    <ul>
                      <li>Python and Basic I/O libraries</li>
                      <li>XECryption (Practice algorithm for encryption (not used <i>anywhere</i> in practice)</li>
                      <li>Lots of time trying to find the pattern in the algorithm</li>
                    </ul>
                  </td>
                </tr>
                <tr>
                  <th>Outcomes: </th>
                  <td>I learned all about hashing, salts, and how to safely encrypt a document. I also learned a lot about how I like to approach a really complicated problem.</td>
                </tr>
              </table>
              <div><img src="img/xecryption_screenshot.png" alt="XECryption Cracking Tool Screenshot"></div>
            </div>
            <p>
              This project was a great way for me to be introduced to the topic of encryption. It was a programming challenge provided by <a href="http://hackthissite.org">this site</a> whose focus is on teaching basic website security techniques.
            </p>
            <p>
              The challenge was to take an encrypted document (provided above) and find a way to decrypt it. The only other info I was given was a PHP page where you enter text into a field and it will encrypt it. Using this I was able to identify a pattern based on the ASCII values of the characters. It turned out that the encryption password wasn't <i>really</i> needed. All that was needed was the final integer value calculated from the password, added to each ASCII value for every character in the text.
            </p>
            <p>
              I then did some math to calculate a floor and ceiling value for this magic "salt" that was generated by the password. Then the tool decrypts using each possible password value and whichever one returns a legible result is the correct password. At no point did I ever find out what the password used to encrypt it was, however it didn't matter - I just needed the salt value provided.
            </p>
            <h3>Conclusions:</h3>
            <p>
              After doing this exercise (took about 3 or 4 hours), I realized just how vital it is to have good security. If I could do this with no prior knowledge of the XECryption algorithm, imagine what a seasoned veteran could do with a real system. This opened my eyes and taught me to always thoroughly research any project's security implementation and ensure that the product I am delivering is high quality and secure to the best of my knowledge.
            </p>
