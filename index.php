            <p>
              This is probably the most challenging problem I have ever tackled. It started on a bus ride home to visit my parents when all I had for entertainment was my linguistics textbook and my computer. I started toying around with trying to interpret the meaning of sentences using a simple Python script.
            </p>
            <p>
              The script got complicated in a hurry and I soon realized I should tackle this using a stricter language like C#. I 'translated' my algorithm to C#, and continued development. I hooked into the <a href="http://dictionaryapi.com">Merriam-Webster Inc.</a> API to automatically define and categorize any words I didn't have in my hand-written dictionary. This greatly expanded the vocabulary of my words (by about 250k words!) and enabled me to experiment with totally novel phrases.
            </p>
            <p>
              After a few weeks and some helpful tips from my Linguistics Professor <a href="http://www.queensu.ca/llcu/people/faculty/charlotte-reinholtz">Dr. Reinholtz</a>, I had my program able to receive an input phrase and recursively generate tree structures for simple phrases.
            </p>
            <p>
              I was shocked when on one conversation with Dr. Reinholtz I mentioned my program had an issue where it was (so I thought) misclassifying a part of the tree as a Determiner-Phrase rather than a specifier for a Noun-Phrase. It turned out that this was not a bug, but rather a real feature of the English language! My algorithm had predicted a language feature that I didn't know about initially. How cool!
            </p>
            <h3>The long term:</h3>
            <p>
              Eventually I want to begin to experiment with developing an algorithm to analyse a simple question and utilizing this library to generate something of a semantic response. I'd be very interested to see what direction this will take my learning and development of this project.
            </p>
