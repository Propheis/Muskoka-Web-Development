                <tr>
                  <th>Outcomes:</th>
                  <td>This project is still in its baby stages however it is capable of outputting relatively accurate syntax trees using the <a href="https://en.wikipedia.org/wiki/X-bar_theory">X-Bar</a> theory. It automatically looks up any input it doesn't understand in a dictionary online and caches the definition for future use.</td>
